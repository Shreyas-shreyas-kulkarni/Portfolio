function menubarfuction() {
    let menulist = document.querySelector(".menulist")
    let exitimage = document.querySelector(".exit")
    let barline = document.querySelector(".navimg")
    let home = document.querySelector(".home")
    // menulist.style.transition = "1s";
    menulist.style.visibility = "visible";
    exitimage.style.visibility = "Visible";
    barline.style.visibility = "hidden";
    // home.style.visibility = "hidden";
    home.style.filter = "blur(10px)";
    document.body.style.overflow = "hidden";

}
